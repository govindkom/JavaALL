package IMPPrograms;

public class SwitchExample {

	public static void main(String[] args) {
		// SwitchExample

		int num=20;
				switch(num) {
				
				case 10:
					System.out.println("case is 10");
					break;
					
				case 20:
					System.out.println("case is 20");
					break;
					
				case 30:
					System.out.println("case is 30");
					break;
					
				default:
					System.out.println("case is not present");
					
				}
	}

}
